function T = ColorVirtual(R,G,B)

T=0;

if (R==0)&&(B==255)&&(G>0)&&(G<255)
     T=G*64/255;  
elseif (R==0)&&(G==255)&&(B>0)&&(B<255)
     T=128+B*((-128+64)/255);  
elseif (B==0)&&(G==255)&&(R>0)&&(R<255)
     T=128+R*(191-128)/255;
elseif (B==0)&&(R==255)&&(G>0)&&(G<255)
     T=255+G*((-255+191)/255);      
end


end

